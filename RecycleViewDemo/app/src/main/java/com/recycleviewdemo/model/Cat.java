package com.recycleviewdemo.model;

public class Cat {
    private String name;
    private int img;

    public Cat(int img, String name) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
